mark=int(input("Enter your marks : "))
if(90<=mark<=100) :
    print("your grade is A")
elif(80<=mark<=89) :
    print("your grade is B")
elif(70<=mark<=79) :
    print("your grade is C")
elif(60<=mark<=69) :
    print("your grade is D")
elif(0<=mark<=59) :
    print("your grade is F")
else :
    print("Invalid number")